#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*res;

	i = 0;
	res = (char *)b;
	while (len--)
	{
		res[i] = c;
		i++;
	}
	return (res);
}
